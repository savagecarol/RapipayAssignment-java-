package Assignment1;

public class ContactDetails {
	
	String houseNumber;
	String locality;
	String cityName;
	String stateName;
	String countryName;
	String pincode;
	String mobileNumber;
	long emailId;
	
	
	ContactDetails
	(String houseNumber,
	String locality,
	String cityName,
	String stateName,
	String countryName,
	String pincode,
	String mobileNumber,
	long emailId)
	{
		this.houseNumber = houseNumber;
		this.locality = locality;
		this.cityName = cityName;
		this.stateName = stateName;
		this.countryName = countryName;
		this.pincode = pincode;
		this.mobileNumber = mobileNumber;
		this.emailId = emailId;
	}

}
